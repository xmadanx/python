a = 5

#and returns true if both are true

print(a > 2 and a < 7)    # True

print(a > 6 and a < 10)   # False

print(a > 10 and a < 4)   # False

#or returns true if one is true

print(a > 2 or a < 7)    # True

print(a > 6 or a < 10)   # True

print(a > 10 or a < 4)   # False

#not returns false if result is true

print(not(a > 10 or a < 4))   # True



